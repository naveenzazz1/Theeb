import UIKit

extension UIColor
{
    
    func returnColorBlue() -> UIColor
    {
        return UIColor(red: 29.0/255.0 , green: 69.0/255.0, blue: 120.0/255.0, alpha: 1.0)
    }
    
    func returnTheebInstantRentalGrey() -> UIColor
    {
        return UIColor(red: 172.0/255.0 , green: 172.0/255.0, blue: 172.0/255.0, alpha: 1.0)
    }
    func returnTheebInstantRentalLightBlue() -> UIColor
    {
        return UIColor(red: 69.0/251.0 , green: 102.0/251.0, blue: 145/251.0, alpha: 1.0)
    }
}
